// Comparison Operators => true or false
// Comparison Operators => > , < , >= , <= , == , === , != , !==
const a = 10;
const b = 10;

// Greater than
console.log(a > b);
// Greater than or equal to
console.log(a >= b);
// Less than
console.log(a < b);
// Less than or equal to
console.log(a <= b);
// Equal to (compares values only)
console.log(5 == "5"); // reuturns true
console.log(a == b);
// Strict equal to (compares values and data types) prefer this
console.log(5 === "5"); // returns false
console.log(a === b);
// Not equal to
console.log(a != b);
// Strict inequality (compares values and data types)
console.log(a !== b);

// The Good Ones: === , !==
// The Evil Twins: == , !=

